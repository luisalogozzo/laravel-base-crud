     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        $cat = new Cat;
        $cat->name = $data['name'];
        $cat->age = $data['age'];
        $cat->race = $data['race'];
        $cat->weight = $data['weight'];
        $cat->color = $data['color'];
        $saved = $cat->save();

        if ($saved == true) {
          return redirect()->route('cats.index');
        }
    }

